#Definiendo una variable con snake_case
nombre_completo= "Fede Marazzi"

#concatenar con +
bienvenida="Hola " +nombre_completo +" ¿Como estas?"

#concatenar con f-strings
bienvenida=f"Hola {nombre_completo} ¿Cómo estás?"

print(bienvenida)

#operadores de pertenencia (in /not in)

print ("Fede" in bienvenida) #true

print ("Fede" not in bienvenida) #false